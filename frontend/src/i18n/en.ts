// Structural type of the dictionary with leaf strings widened to `string`,
// so translated dictionaries (zh) keep the exact key shape but supply their
// own values rather than being forced to match the English literals.
type Localized<T> = {
  [K in keyof T]: T[K] extends string ? string : Localized<T[K]>
}

export type Dict = Localized<typeof en>
